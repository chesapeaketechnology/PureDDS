/**
 * Miscellaneous utils.
 *
 * @author Matt Coley
 */
public class MiscUtil {
	/**
	 * @return Empty byte array.
	 */
	public static byte[] emptyBytes() {
		return new byte[0];
	}

	/**
	 * @return Empty string.
	 */
	public static String emptyString() {
		return "";
	}
}
